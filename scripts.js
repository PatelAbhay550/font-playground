document
  .getElementById("font-search-input")
  .addEventListener("input", function () {
    const inputValue = this.value.toLowerCase();
    const dataList = document.getElementById("font-suggestions");
    dataList.innerHTML = "";

    const matchingFonts = fontSuggestions.filter((font) =>
      font.toLowerCase().includes(inputValue)
    );

    matchingFonts.forEach((font) => {
      const option = document.createElement("option");
      option.value = font;
      dataList.appendChild(option);
    });
  });
function handleKeyPress(event) {
  if (event.key === "Enter") {
    searchFont();
  }
}
function uploadFont(event) {
  const fileInput = event.target;
  const fileList = fileInput.files;

  if (fileList.length > 0) {
    const fontFile = fileList[0];
    // Perform logic to load and use the uploaded font
    console.log("Uploaded font:", fontFile.name);
    applyFont(fontFile.name, fontFile);
  }
}

function applyFont(fontName, fontFile) {
  const fontStyles = document.getElementById("custom-font-styles");

  if (fontFile) {
    // Upload and use the custom font file
    const fontUrl = URL.createObjectURL(fontFile);
    fontStyles.innerHTML = `@font-face {
            font-family: '${fontName}';
            src: url('${fontUrl}') format('woff2');
        }`;
  } else {
    // Use a Google Font
    fontStyles.innerHTML = `@import url('https://fonts.googleapis.com/css2?family=${fontName.replaceAll(
      " ",
      "+"
    )}&display=swap');`;
  }

  // Apply the font to the sections
  const sections = document.querySelectorAll(".section");
  sections.forEach((section) => {
    section.style.fontFamily = `'${fontName}', sans-serif`;
  });
}
