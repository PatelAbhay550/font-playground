function searchFont() {
  const searchTerm = document.getElementById("font-search-input").value;
  applyFont(searchTerm);
}

function applyFont(fontFamily) {
  const styleElement = document.createElement("style");
  styleElement.textContent = `@import url('https://fonts.googleapis.com/css2?family=${fontFamily.replaceAll(
    " ",
    "+"
  )}:wght@400;700&display=swap');`;

  // Remove existing styles
  const existingStyleElement = document.querySelector(
    "style[data-font-styles]"
  );
  if (existingStyleElement) {
    existingStyleElement.remove();
  }

  // Add the new style element
  styleElement.setAttribute("data-font-styles", true);
  document.head.appendChild(styleElement);

  document.body.style.fontFamily = fontFamily;
}

